export function flow(
  firstFunction: (...params: unknown[]) => unknown,
  ...functions: ((value: unknown) => unknown)[]
): (...params: unknown[]) => unknown {
  return (...params) => {
    return functions.reduce((val, fn) => fn(val), firstFunction(...params));
  };
}
